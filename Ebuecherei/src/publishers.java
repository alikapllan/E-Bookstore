    void show_publishers_live(){
        try {
            
            ArrayList<publisher> publisher_list = get_publisher_datas();
            DefaultTableModel model = (DefaultTableModel)publishers_table.getModel();
                model.setRowCount(0);
            Object[] row = new Object[4];
        
        for(int i=0; i<publisher_list.size();i++){
            row[0] = publisher_list.get(i).getPublisher_id();
            row[1] = publisher_list.get(i).getPublisher_name();
            row[2] = publisher_list.get(i).getPublisher_url();
            row[3] = publisher_list.get(i).getPublisher_email();
            
            //with this method we allow to all AUTHORS existing in the database live
            model.addRow(row);
        }
        } catch (Exception e) {
            System.out.println(e);
        }
        
    }
    
    ArrayList<publisher> get_publisher_datas(){
        
        // we will save here all publisher datas existing in the db
        ArrayList<publisher> publishers = new ArrayList<>();
        
        String SQL_expression ="SELECT publisher_id,publisher_name,publisher_url,publisher_email FROM publisher";
        
        try {
            
            postresql_db_connection db_connection = new postresql_db_connection();
            PreparedStatement st = db_connection.connect().prepareStatement(SQL_expression);
            
            ResultSet rs;
            rs = st.executeQuery();
            
            //an object from author class so that we can save all book publishers existing in database
            publisher publisher;
            
            while(rs.next()){
                publisher = new publisher(rs.getInt("publisher_id"),rs.getString("publisher_name"),rs.getString("publisher_url"),rs.getString("publisher_email"));
                publishers.add(publisher);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return publishers;
    }
