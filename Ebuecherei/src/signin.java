    boolean inputCheck(){
        String nickname = nickname_field.getText();
        String password = String.valueOf(password_field.getPassword());
        String name = name_field.getText();
        String surname = surname_field.getText();
        String email = email_field.getText();
        String phone = phone_field.getText();
        String street = street_field.getText();
        String house_number = house_number_field.getText();
        String postcode = postcode_field.getText();
        String city = city_field.getText();
        
        if(nickname.trim().equals("") || password.trim().equals("") || name.trim().equals("") || surname.trim().equals("") ||
                email.trim().equals("") || phone.trim().equals("") || street.trim().equals("") || 
                house_number.trim().equals("") || postcode.trim().equals("") || city.trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "All fields have to be fullfilled.","ERROR AT INPUT",2);
            return false;
        }
        return true;
    }
    
    boolean user_existence_check(String nickname){
        boolean user_existence = false;
        
        String SQL_expression = "SELECT * FROM customer WHERE customer_nickname=?";
        
        try {
            postresql_db_connection db_Connection = new postresql_db_connection();
            PreparedStatement st = db_Connection.connect().prepareStatement(SQL_expression);
            ResultSet rs;
            
            st.setString(1, nickname);
            rs = st.executeQuery();
            
            if(rs.next()){
                user_existence = true;
                JOptionPane.showMessageDialog(null, "This Nickname has already been taken. Please Change it!","Nickname ERROR",2);
                nickname_field.setText("");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        return user_existence;
    }
