    private void signin_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signin_buttonActionPerformed
        String nickname = nickname_field.getText();
        String password = String.valueOf(password_field.getPassword());
        String name = name_field.getText();
        String surname = surname_field.getText();
        String email = email_field.getText();
        String phone = phone_field.getText();
        boolean gender = true;
            if(female_button.isSelected()){
                gender = false;
            }
        String street = street_field.getText();
        String house_number = house_number_field.getText();
        String postcode = postcode_field.getText();
        String city = city_field.getText();
        
        if (inputCheck()) {
                if(!user_existence_check(nickname)){
                    String SQL_expression = "INSERT INTO customer(customer_nickname,customer_password,customer_name,customer_surname,"
                    + "customer_email,customer_phone,customer_gender,customer_street,customer_hause_number,customer_postcode,customer_city) VALUES(?,?,?,?,?,?,?,?,?,?,?)";
            
                    // Step 1: Establishing a Connection
                    try {
                        postresql_db_connection db_Connection = new postresql_db_connection();
                        PreparedStatement st = db_Connection.connect().prepareStatement(SQL_expression);

                        st.setString(1, nickname);
                        st.setString(2, password);
                        st.setString(3, name);
                        st.setString(4, surname);
                        st.setString(5, email);
                        st.setString(6, phone);
                        st.setBoolean(7, gender);
                        st.setString(8, street);
                        st.setInt(9, Integer.valueOf(house_number));
                        st.setInt(10, Integer.valueOf(postcode));
                        st.setString(11, city);

                        if(st.executeUpdate() != 0){
                            st.close();
                            JOptionPane.showMessageDialog(null, "New Customer is created!");
                            dispose();
                            new login().setVisible(true);
                        }

                    } catch (Exception e) {
                        System.out.println(e);
                    }
                }
        }
    }//GEN-LAST:event_signin_buttonActionPerformed
       
